    // listaUnEan
    public Vector listaUnEan() {

        //Averigua en tiempo de ejecucion cual es el nombre del archivo en ejecucion
        Class tipoObjeto = this.getClass();
        String nombreClase = tipoObjeto.getName();

        Vector contenedor = new Vector();
        Connection connection = null;

        String selectStr =
                "SELECT tblPlusEan.ean,      " +
                "       tblPlusEan.idPlu,    " +
                "       tblPlusEan.estado,   " +
                "       tblPlusEan.idSeq     " +
                "FROM tblPlusEan             " +
                "WHERE tblPlusEan.idPlu  =   " +
                getIdPlu() + "               " +
                "AND   tblPlusEan.estado = 1 " +
                "ORDER BY tblPlusEan.ean     ";

        PreparedStatement selectStatement = null;

        try {

            // Obtiene una conexion a la base de datos, requiere Try
            connection = jdbcAccess.getConnection();

            // Prepara la sentencia de Busqueda
            selectStatement = connection.prepareStatement(selectStr);

            // Obtiene el resultset
            ResultSet rs = selectStatement.executeQuery();

            // Variable de fachada de los datos
            FachadaPluEanBean fachadaPluEanBean;

            while (rs.next()) {

                //
                fachadaPluEanBean = new FachadaPluEanBean();

                //
                fachadaPluEanBean.setEan(rs.getString("ean"));
                fachadaPluEanBean.setIdPlu(rs.getInt("idPlu"));
                fachadaPluEanBean.setEstado(rs.getInt("estado"));
                fachadaPluEanBean.setIdSeq(rs.getInt("idSeq"));

                //
                contenedor.add(fachadaPluEanBean);

            }

            // Cierra el Resultset
            rs.close();

        } catch (NamingException ne) {
            System.out.println("NamingException in " + nombreClase + " " + ne);
        } catch (SQLException sqle) {

            System.out.println("SQLException in" + nombreClase);

            String sqlMessage = sqle.getMessage();
            String sqlState = sqle.getSQLState();
            int vendorCode = sqle.getErrorCode();
            System.out.println("Ocurrio una  excepcion");
            System.out.println("Mensaje " + sqlMessage);
            System.out.println("SQL State " + sqlState);
            System.out.println("Vendor Code " + vendorCode);

        } catch (Exception e) {
            System.out.println("Exception In :" + nombreClase + " " + e);
        } finally {
            jdbcAccess.cleanup(connection, selectStatement, null);
            return contenedor;
        }
    }

